async function downloadImage(url: string, filepath: string, retries = 3): Promise<void> {
	// –ü—Ä–æ–ø—É—Å–∫–∞–µ–º, –µ—Å–ª–∏ —Ñ–∞–π–ª —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏ –Ω–µ –ø—É—Å—Ç–æ–π
	if (fs.existsSync(filepath)) {
		const stats = fs.statSync(filepath)
		if (stats.size > 0) {
			return Promise.resolve()
		}
	}

	return new Promise((resolve, reject) => {
		https
			.get(url, (response) => {
				// –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º —Ä–µ–¥–∏—Ä–µ–∫—Ç—ã (301, 302, 307, 308)
				if (
					response.statusCode &&
					response.statusCode >= 300 &&
					response.statusCode < 400 &&
					response.headers.location
				) {
					// –°–ª–µ–¥—É–µ–º —Ä–µ–¥–∏—Ä–µ–∫—Ç—É
					downloadImage(response.headers.location, filepath).then(resolve).catch(reject)
					return
				}

				// –ü—Ä–æ–≤–µ—Ä—è–µ–º —É—Å–ø–µ—à–Ω—ã–π —Å—Ç–∞—Ç—É—Å
				if (response.statusCode !== 200) {
					reject(new Error(`Failed to download image: status ${response.statusCode}`))
					return
				}

				const file = fs.createWriteStream(filepath)
				response.pipe(file)
				file.on('finish', () => {
					file.close()
					resolve()
				})
				file.on('error', (err) => {
					fs.unlink(filepath, () => {})
					reject(err)
				})
			})
			.on('error', async (err) => {
				// –ü–æ–≤—Ç–æ—Ä–Ω–∞—è –ø–æ–ø—ã—Ç–∫–∞ –ø—Ä–∏ –æ—à–∏–±–∫–µ —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è
				if (
					retries > 0 &&
					(err.message.includes('ECONNRESET') || err.message.includes('ETIMEDOUT'))
				) {
					await delay(1000) // –∑–∞–¥–µ—Ä–∂–∫–∞ 1 —Å–µ–∫—É–Ω–¥–∞
					try {
						await downloadImage(url, filepath, retries - 1)
						resolve()
					} catch (retryErr) {
						reject(retryErr)
					}
				} else {
					reject(err)
				}
			})
	})
}

