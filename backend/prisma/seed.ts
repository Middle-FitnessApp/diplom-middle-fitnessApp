function formatTime(seconds: number): string {
	if (seconds < 60) return `${Math.round(seconds)}—Å`
	const minutes = Math.floor(seconds / 60)
	const secs = Math.round(seconds % 60)
	return `${minutes}–º ${secs}—Å`
}

