async function downloadImage(url: string, filepath: string, retries = 3): Promise<void> {
	// –ü—Ä–æ–ø—É—Å–∫–∞–µ–º, –µ—Å–ª–∏ —Ñ–∞–π–ª —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏ –Ω–µ –ø—É—Å—Ç–æ–π
	if (fs.existsSync(filepath)) {
		const stats = fs.statSync(filepath)
		if (stats.size > 0) {
			return
		}
	}

	return new Promise((resolve, reject) => {
		const file = fs.createWriteStream(filepath)

		const request = https.get(url, (response) => {
			// –û–±—Ä–∞–±–æ—Ç–∫–∞ —Ä–µ–¥–∏—Ä–µ–∫—Ç–æ–≤
			if (response.statusCode === 302 || response.statusCode === 301) {
				const redirectUrl = response.headers.location
				if (redirectUrl) {
					file.close()
					fs.unlinkSync(filepath) // –£–¥–∞–ª—è–µ–º –ø—É—Å—Ç–æ–π —Ñ–∞–π–ª
					downloadImage(redirectUrl, filepath, retries).then(resolve).catch(reject)
					return
				}
			}

			if (response.statusCode !== 200) {
				file.close()
				fs.unlinkSync(filepath)
				if (retries > 0) {
					setTimeout(() => {
						downloadImage(url, filepath, retries - 1)
							.then(resolve)
							.catch(reject)
					}, 1000)
				} else {
					reject(new Error(`Failed to download ${url}: ${response.statusCode}`))
				}
				return
			}

			response.pipe(file)

			file.on('finish', () => {
				file.close()
				resolve()
			})

			file.on('error', (err) => {
				file.close()
				fs.unlinkSync(filepath)
				if (retries > 0) {
					setTimeout(() => {
						downloadImage(url, filepath, retries - 1)
							.then(resolve)
							.catch(reject)
					}, 1000)
				} else {
					reject(err)
				}
			})
		})

		request.on('error', (err) => {
			file.close()
			fs.unlinkSync(filepath)
			if (retries > 0) {
				setTimeout(() => {
					downloadImage(url, filepath, retries - 1)
						.then(resolve)
						.catch(reject)
				}, 1000)
			} else {
				reject(err)
			}
		})

		request.setTimeout(30000, () => {
			file.close()
			fs.unlinkSync(filepath)
			if (retries > 0) {
				setTimeout(() => {
					downloadImage(url, filepath, retries - 1)
						.then(resolve)
						.catch(reject)
				}, 1000)
			} else {
				reject(new Error(`Timeout downloading ${url}`))
			}
		})
	})
}

